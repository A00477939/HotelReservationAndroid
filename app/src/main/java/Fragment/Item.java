package Fragment;

public class Item {

    String name;
    String price;
    int image;

    public Item(String name, String email, int image) {
        this.name = name;
        this.price = email;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String email) {
        this.price = price;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}